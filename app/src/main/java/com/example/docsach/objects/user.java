package com.example.docsach.objects;

public class user {
    String name, avata;

    public user(String name, String avata) {
        this.name = name;
        this.avata = avata;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAvata() {
        return avata;
    }

    public void setAvata(String avata) {
        this.avata = avata;
    }
}
